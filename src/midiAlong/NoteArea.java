package midiAlong;

public class NoteArea {
	private int x;
	private int end;
	private int y;
	
	public void setX(int x1) {
		x = x1;
	}
	
	public int getX() {
		return x;
	}
	
	public void setEnd(int end1) {
		end = end1;
	}
	
	public int getEnd() {
		return end;
	}
	
	public void setY(int y1) {
		y = y1;
	}
	
	public int getY() {
		return y;
	}
}
